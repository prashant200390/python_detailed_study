# Write a program to print the content of a list , tuple and string using for loops.

list = ["apple",34,"ballon",39,22,55,0.34,"cat","meow","purr"]
for i in list:              # for list
    print(i) 

tuple = ("apple",44,"cat","dog",'woof')
for i in tuple:             # for tuple
    print(i)

string = "encyclopidea"
for i in string:             # for string 
    print(i)