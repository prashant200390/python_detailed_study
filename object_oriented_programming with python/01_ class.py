class employee():
    language = "python"
    salary = 1400000

prashant = employee()
print(prashant.language , prashant.salary)

prabin = employee()
print(prabin.salary , prabin.language)

# the attributes belongs to class are called class attributes and those which belongs to object(instance) (fyi outside the class , here prashant and prabin ) are called object attribute or instance attribute .